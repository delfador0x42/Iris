// IrisWiFi - WiFi monitoring using CoreWLAN
//
// This package provides WiFi monitoring capabilities for Iris,
// replicating and enhancing the functionality of Apple's airport CLI tool.
//
// Key Components:
// - WiFiStore: Main state store for WiFi monitoring
// - WiFiInterfaceInfo: Current interface state model
// - WiFiNetwork: Scanned network model
// - WiFiPreferences: WiFi join/admin preferences model
// - WiFiTypes: Enums for security, PHY modes, channels, etc.
// - WiFiMonitorView: Main WiFi monitoring UI
// - WiFiPreferencesView: Preferences settings UI

